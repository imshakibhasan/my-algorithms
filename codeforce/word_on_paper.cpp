#include<bits/stdc++.h>
using namespace std;
int main() {
    int t;
    cin>>t;
    while (t--) {
        int i, j, flag = 0;
        char arr[8][8];
        for (i = 0; i < 8; i++) {
            for (j = 0; j < 8; j++) {
                cin>>arr[i][j];
            }
        }
        for (i = 0; i < 8; i++) {
            for (j = 0; j < 8; j++) {
                if (arr[j][i] != '.') {
                    cout<<arr[j][i];
                    flag++;
                }
            }
            if (flag > 0) {
                break;
            }
        }
        cout<<endl;
    }
}

